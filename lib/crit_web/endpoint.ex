defmodule CritWeb.Endpoint do
  use Phoenix.Endpoint, otp_app: :crit

  # Adds `Cache-Control: no-transform` to every response (including LiveView
  # socket transport endpoints). See `CritWeb.Plugs.NoTransform` and issue #50.
  @before_compile CritWeb.Plugs.NoTransform

  # The session will be stored in the cookie and signed,
  # this means its contents can be read but not tampered with.
  # Set :encryption_salt if you would also like to encrypt it.
  @session_options [
    store: :cookie,
    key: "_crit_key",
    signing_salt: "vMlH9JxO",
    same_site: "Lax"
  ]

  @session_init Plug.Session.init(@session_options)
  @secure_session_init Plug.Session.init(Keyword.put(@session_options, :secure, true))

  socket "/live", Phoenix.LiveView.Socket,
    websocket: [connect_info: [:peer_data, :uri, :user_agent, session: @session_options]],
    longpoll: [connect_info: [:peer_data, :uri, :user_agent, session: @session_options]]

  # Serve at "/" the static files from "priv/static" directory.
  #
  # When code reloading is disabled (e.g., in production),
  # the `gzip` option is enabled to serve compressed
  # static files generated by running `phx.digest`.
  plug Plug.Static,
    at: "/",
    from: :crit,
    gzip: not code_reloading?,
    only: CritWeb.static_paths(),
    only_matching: CritWeb.static_paths_matching(),
    raise_on_missing_only: code_reloading?,
    headers: %{"cache-control" => "public, max-age=31536000, immutable"}

  # Code reloading can be explicitly enabled under the
  # :code_reloader configuration of your endpoint.
  if code_reloading? do
    socket "/phoenix/live_reload/socket", Phoenix.LiveReloader.Socket
    plug Phoenix.LiveReloader
    plug Phoenix.CodeReloader
    plug Phoenix.Ecto.CheckRepoStatus, otp_app: :crit
  end

  plug Plug.RequestId
  plug Plug.Telemetry, event_prefix: [:phoenix, :endpoint]

  plug Plug.Parsers,
    parsers: [:urlencoded, :multipart, :json],
    pass: ["*/*"],
    json_decoder: Phoenix.json_library()

  plug Plug.MethodOverride
  plug Plug.Head
  plug CritWeb.Plugs.CanonicalHost
  plug :session

  # Drop request body and cookies — review documents and comment bodies must
  # never leave the deployment. Placed after Session/MethodOverride so the
  # captured context reflects the final method and session state.
  plug Sentry.PlugContext,
    body_scrubber: nil,
    cookie_scrubber: nil

  plug CritWeb.Router

  defp session(conn, _opts) do
    if Application.get_env(:crit, :secure_cookies) do
      Plug.Session.call(conn, @secure_session_init)
    else
      Plug.Session.call(conn, @session_init)
    end
  end
end
